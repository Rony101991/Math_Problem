package mathProblem;

public class Swape_Numbers_With_Third_Variable {
    public static void main(String[] args) {

        int a = 5;
        int b = 20;
        int t;
        t=a; //t=5;
        a=b; //a=20;
        b=t; //b=5;
        System.out.println(a);
        System.out.println(b);
    }
}
